
public class DefaultValuePrimitiveDataType {
	int a;
	float b;
	char c;
	double d;
	long l;
	boolean bool;
	
	public static void main(String[] args) {
		DefaultValuePrimitiveDataType obj = new DefaultValuePrimitiveDataType();
		System.out.println("Default value of Int : "+obj.a);
		System.out.println("Default value of float : "+obj.b);
		System.out.println("Default value of char : "+obj.c);
		System.out.println("Default value of double : "+obj.d);
		System.out.println("Default value of long : "+obj.l);
		System.out.println("Default value of boolean : "+obj.bool);
		
		
	}

}
